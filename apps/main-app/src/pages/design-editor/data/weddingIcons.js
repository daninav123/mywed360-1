/**
 * Obtener todos los iconos en lista plana
 */
export function getAllIcons() {
  const allIcons = [];
  Object.entries(WEDDING_ICONS).forEach(([category, icons]) => {
    icons.forEach(icon => {
      allIcons.push({ ...icon, categoryName: category });
    });
  });
  return allIcons;
}

/**
 * Buscar iconos por término
 */
export function searchIcons(query) {
  const allIcons = getAllIcons();
  const lowerQuery = query.toLowerCase();
  return allIcons.filter(icon => 
    icon.name.toLowerCase().includes(lowerQuery) ||
    icon.categoryName.toLowerCase().includes(lowerQuery)
  );
}

