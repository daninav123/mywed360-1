function getLocales() {
  return fs.readdirSync(LOCALES_DIR)
    .filter(dir => {
      const dirPath = path.join(LOCALES_DIR, dir);
      return fs.statSync(dirPath).isDirectory();
    });
}

function syncFooterKeys() {
  const locales = getLocales();
  let updatedCount = 0;
  let skippedCount = 0;
  let createdCount = 0;

  console.log(`üîÑ Sincronizando claves del footer en ${locales.length} idiomas...\n`);

  locales.forEach(locale => {
    const filePath = path.join(LOCALES_DIR, locale, TARGET_FILE);

    // Si no existe marketing.json, saltar
    if (!fs.existsSync(filePath)) {
      console.log(`‚è≠Ô∏è  ${locale}: marketing.json no existe, saltando`);
      skippedCount++;
      return;
    }

    try {
      const content = fs.readFileSync(filePath, 'utf-8');
      const data = JSON.parse(content);

      // Obtener traducciones para este idioma
      const translations = FOOTER_TRANSLATIONS[locale];
      
      if (!translations) {
        console.log(`‚ö†Ô∏è  ${locale}: No hay traducciones definidas, saltando`);
        skippedCount++;
        return;
      }

      // Verificar si ya existe la secci√≥n footer
      if (!data.footer) {
        data.footer = {};
        console.log(`‚ú® ${locale}: Creando secci√≥n footer`);
        createdCount++;
      }

      // A√±adir claves faltantes
      let keysAdded = 0;
      Object.keys(translations).forEach(key => {
        if (!data.footer[key]) {
          data.footer[key] = translations[key];
          keysAdded++;
        }
      });

      if (keysAdded > 0) {
        // Guardar con formato bonito
        fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf-8');
        console.log(`‚úÖ ${locale}: ${keysAdded} claves a√±adidas`);
        updatedCount++;
      } else {
        console.log(`‚úì  ${locale}: Ya tiene todas las claves`);
      }

    } catch (error) {
      console.error(`‚ùå ${locale}: Error - ${error.message}`);
    }
  });

  console.log(`\nüìä Resumen:`);
  console.log(`   ‚úÖ Actualizados: ${updatedCount}`);
  console.log(`   ‚úì  Sin cambios: ${locales.length - updatedCount - skippedCount}`);
  console.log(`   ‚ú® Nuevas secciones: ${createdCount}`);
  console.log(`   ‚è≠Ô∏è  Saltados: ${skippedCount}`);
}

// Ejecutar
syncFooterKeys();
